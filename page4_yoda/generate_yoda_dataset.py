# ── Сборка ответа ────────────────────────────────────────────────────────────

def build_response(hint: str = "") -> str:
    parts = []

    if random.random() > 0.35:
        parts.append(random.choice(OPENINGS))

    hint_lower = hint.lower()
    if any(w in hint_lower for w in ["сила", "джедай", "тёмн", "темн"]):
        core = random.choice(TEMPLATES_FORCE + TEMPLATES_DARK_SIDE)
    elif any(w in hint_lower for w in ["страх", "боюсь", "боится"]):
        core = random.choice(TEMPLATES_FEAR)
    elif any(w in hint_lower for w in ["покой", "мир", "спокойствие"]):
        core = random.choice(TEMPLATES_PEACE)
    elif any(w in hint_lower for w in ["сильн", "власть", "мощ"]):
        core = random.choice(TEMPLATES_STRENGTH)
    elif any(w in hint_lower for w in ["решени", "выбор", "понять", "знать"]):
        core = random.choice(TEMPLATES_DECISION)
    else:
        core = random.choice(ALL_TEMPLATES)

    parts.append(core)

    if random.random() > 0.55:
        parts.append(f"Помни: {random.choice(FORCE_PHRASES)}")

    if random.random() > 0.30:
        parts.append(random.choice(WISDOM_CLOSINGS))

    return " ".join(parts)


# ── Генераторы пар ───────────────────────────────────────────────────────────

def make_simple_pair() -> tuple[str, str]:
    q = random.choice(ALL_QUESTIONS)
    return q, build_response(q)


