# ── Процесс обучения ─────────────────────────────────────────────────────────
st.header("🛠️ Процесс обучения")

st.markdown(
    """
1. **Данные.** Для классификаторов — реальные отзывы и новости. Для LLM —
   синтетические датасеты (~1200 пар), сгенерированные шаблонами под нужный стиль.
2. **Классификаторы.** Дообучали `rubert-tiny2` через HuggingFace `Trainer`;
   LogReg и LSTM — с нуля. Быстро даже на CPU.
3. **LLM (QLoRA).** Базовый `Qwen2.5-7B` грузили в **4-bit (NF4)**, поверх —
   обучаемый **LoRA-адаптер** (r=16). Так 7B-модель влезает в 16 ГБ и учится
   за ~20–30 минут на RTX 4090.
4. **Деплой.** Арендовали GPU-сервер Selectel (RTX 4090, Ubuntu 24.04),
   подключились через **VS Code Remote SSH**, окружение — `uv`,
   модель — `hf download`. Веса (>100 МБ) переносили через **scp**.
"""
)

with st.expander("📐 Что такое QLoRA и почему именно она"):
    st.markdown(
        """
- **Квантизация 4-bit** ужимает веса базовой модели — 7B влезает в ~6 ГБ VRAM.
- **LoRA** обучает не всю модель, а маленькие низкоранговые «добавки» к слоям
  внимания (всего ~0.5% параметров, ~35M из 7B).
- Базовые веса **заморожены** — поэтому адаптер можно включать/выключать на лету
  и сравнивать «до/после» на одной и той же модели.
- Итоговый адаптер весит ~160 МБ вместо 15 ГБ полной модели.
"""
    )

